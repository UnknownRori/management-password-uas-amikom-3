    public void refreshTableModel() {
        DefaultTableModel TampilTabel = new DefaultTableModel();
        TampilTabel.addColumn("Nama");
        TampilTabel.addColumn("Payload");
        TampilTabel.addColumn("Password");

        repository.passwords.forEach((p) -> {
            TampilTabel.addRow(new Object[]{
                p.name,
                Base64.getEncoder().encodeToString(p.payload),
                p.password.orElse("************"),});
        });

        table1.setModel(TampilTabel);
    }

    public void refreshTable() {
        try {
            repository.all(MasterPasswords.connection.get());
            refreshTableModel();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Gagal mengambil data!");
        } catch (NoSuchElementException ex) {
            JOptionPane.showMessageDialog(null, "Koneksi Mysql tidak ada!");
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan!");
        }
    }
    
    private String getNameFromTable() {
        int row = table1.getSelectedRow();
        int col = 0;
        return table1.getModel().getValueAt(row, col).toString();
    }

