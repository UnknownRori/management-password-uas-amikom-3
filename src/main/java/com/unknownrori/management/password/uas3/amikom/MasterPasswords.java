    private void cmdBukaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdBukaActionPerformed
        String val = getNameFromTable();
        String master = JOptionPane.showInputDialog("Masukan Master Password");

        try {
            MasterPasswords.repository.decrypt(MasterPasswords.connection.get(), val, master);
            refreshTableModel();
        } catch (NoSuchElementException ex) {
            System.out.println(ex);
            JOptionPane.showMessageDialog(null, "Koneksi Mysql tidak ada!");
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan!");
        }
    }//GEN-LAST:event_cmdBukaActionPerformed

